// Define a new interface named Vehicle
public interface Vehicle {

    // Declare a method to get the make of the vehicle, which returns a String
    String getMake();

    // Declare a method to get the model of the vehicle, which returns a String
    String getModel();

    // Declare a method to get the year of the vehicle, which returns an int
    int getYear();

    // Declare a method to set the make of the vehicle, which takes a String as a parameter
    void setMake(String make);

    // Declare a method to set the model of the vehicle, which takes a String as a parameter
    void setModel(String model);

    // Declare a method to set the year of the vehicle, which takes an int as a parameter
    void setYear(int year);
}
